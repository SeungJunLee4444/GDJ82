package ex01_variable;

public class Ex02 {

	public static void main(String[] args) {
		
		// 문자열 타입
		// 기본타입은 아니지만, 자주 사용되기 떄문에 자바에서 기본타입처럼 지원
		// String 클래스 지원
		
		String country = "한국"; // 자바가 쉬운 사용법을 지원
		String name = new String("민경태"); // 원래 사용법  ***
		// 자주 사용되기 때문에 원래 사용법 외에 기본타입처럼 쉬운 사용법을 지원
		System.out.println(country);
		System.out.println(name);
	}

}
